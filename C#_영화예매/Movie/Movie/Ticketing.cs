using System;

namespace Movie
{
    class Ticketing
    {
        private String id;
        private String title;
        private String seat;
        private String date;

        public string Id { get => id; set => id = value; }
        public string Title { get => title; set => title = value; }
        public string Seat { get => seat; set => seat = value; }
        public string Date { get => date; set => date = value; }
    }
}
